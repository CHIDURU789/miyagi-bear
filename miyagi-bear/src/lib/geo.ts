// 市町村名から座標を取得（前方一致・部分一致フォールバック付き）
export function getCityCoords(city: string): [number, number] {
  // 完全一致
  if (MIYAGI_CITY_COORDS[city]) return MIYAGI_CITY_COORDS[city];

  // 前方一致（例: "仙台市青葉区XXX" → "仙台市青葉区"）
  for (const key of Object.keys(MIYAGI_CITY_COORDS)) {
    if (city.startsWith(key) || key.startsWith(city)) {
      return MIYAGI_CITY_COORDS[key];
    }
  }

  // 部分一致
  for (const key of Object.keys(MIYAGI_CITY_COORDS)) {
    if (city.includes(key.replace(/[市町村区郡]$/, ""))) {
      return MIYAGI_CITY_COORDS[key];
    }
  }

  // 宮城県中心を返す
  return [38.5, 140.9];
}

